        <p>  We follow an approach of Participative and Experiential Learning, where students are not merely receiving knowledge rather they get to co-create knowledge with the trainers through ”In Class” sessions and Lab sessions.</p>
<p>The Knowledge of a Concept is first inculcated in them through “In Class” sessions and discussions followed by application of the concept to a practical design scenario in the Lab sessions. The hands on Lab sessions are designed to make students explore the various dimensions of a concept in a design scenario. Subsequently they develop the ability to apply concepts to more complex design scenarios through Live Projects they get to work at, as a part of the program curriculum.</p>
<p>This Comprehensive methodology of acquiring skills, gaining hands on practical experience and productivity cannot be achieved through e-learning platforms as they cannot induce a participative and experiential learning experience for students.</p>
